// slice_start_stop.js

let people = ["Melissa", "Jennifer", "Tabitha", "Jane"];

// start and stop indices
let start = 0;
let stop = 2;

// loop from start to stop, printing each element
for (let i = start; i < stop; i++)
{
    console.log(people[i]);
}

